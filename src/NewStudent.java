    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
      
        try {
            con = DriverManager.getConnection(host, username, password);
            pst = con.prepareStatement("INSERT INTO detail (StudentId,Name,FatherName,CourseName,BranchName)values(?,?,?,?,?)");
            pst.setString(1, StudentID.getText());
            pst.setString(2, name.getText());
            pst.setString(3, Fathername.getText());
            pst.setString(4, txtcourse.getSelectedItem().toString());
            pst.setString(5, txtbranch.getSelectedItem().toString());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Signup Successful");

          }
          catch (SQLException e){
              JOptionPane.showMessageDialog(null, "Error"+e);
          }
                                           

    }//GEN-LAST:event_SaveActionPerformed

